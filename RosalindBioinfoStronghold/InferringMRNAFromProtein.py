def codon_frequencies():
    frequencies = {}
    for k, v in RNA_CODON_TABLE.items():
        if not v in frequencies:
            frequencies[v] = 0
        frequencies[v] += 1
    return frequencies


def possible_rna_strings(input):
    f = codon_frequencies()
    n = f['Stop']

    for c in input:
        n *= f[c]

    return n


